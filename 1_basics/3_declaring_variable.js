/*
* variable 선언하기
* 1) var - 더이상 쓰지 않는다
* 2) let
* 3) const
* */

var name = '코트팩토리';
console.log(name)

let ive = '아이브';
console.log(ive);

/*
* let과 var로 선언하면 추후 변경할 수 있다.
*
* */

const newJeans = '코트팩토리';
console.log(newJeans);

/* const의 경우 값을 변경할 수 없다. */


